class RealGitWorktreeTest(unittest.TestCase):
    """RLY-173, and a deliberate change of testing style.

    Every executor/server bug found during the first live cutover (RLY-163, 165, 166, 170, and
    a regression in my own RLY-166 guard) shared one cause: each side was tested against its
    OWN IMAGINED version of the other's contract. The W10 review named it — 'the tests
    hand-build job dicts in the executor's imagined shape, so they pass while the real
    integration cannot work'. Mocking `subprocess.run` here would test beliefs about git.

    So these tests drive a REAL git repository in a temp dir. If the executor's idea of what
    `git checkout` does is wrong, this fails; a mock would happily agree with the mistake.
    """

    def setUp(self):
        import tempfile, shutil
        self.dir = tempfile.mkdtemp(prefix="relay-git-test-")
        self.addCleanup(shutil.rmtree, self.dir, ignore_errors=True)
        self.git("init", "-q", "-b", "main")
        self.git("config", "user.email", "t@example.com")
        self.git("config", "user.name", "T")
        self.write("seed.txt", "seed")
        self.git("add", "-A"); self.git("commit", "-qm", "seed")
        self.control = relay.JobControl()

    def git(self, *args):
        return relay.subprocess.run(["git", *args], cwd=self.dir, capture_output=True, text=True)

    def write(self, name, text):
        with open(os.path.join(self.dir, name), "w") as f:
            f.write(text)

    def head_ref(self):
        return self.git("symbolic-ref", "-q", "HEAD").stdout.strip()

    def test_an_exclusive_job_reattaches_a_detached_worktree_to_its_branch(self):
        """The RLY-170 drill's exact end state: the orphan is requeued and re-claimed, but the
        restart left the worktree detached, so the guard refused forever. The run's commits are
        still on the branch — HEAD just needs pointing back at them."""
        branch = "rly-1-feature"
        self.git("checkout", "-q", "-b", branch)
        self.write("work.txt", "committed before the interruption")
        self.git("add", "-A"); self.git("commit", "-qm", "run work")
        wanted = self.git("rev-parse", "HEAD").stdout.strip()

        # What reset_worktree() leaves behind: detached, branch ref intact.
        self.git("checkout", "-q", "--detach", "main")
        self.assertEqual(self.head_ref(), "", "precondition: worktree is detached")

        j = job("exclusive_shell", run="true", id="nj-1", run_id="r1",
                vars={"ref": "RLY-1", "branch": branch})
        outcome, detail, sha, _session = relay.run_node_job(j, self.dir, self.control)

        self.assertEqual(outcome, "succeeded", detail)
        self.assertEqual(self.head_ref(), "refs/heads/" + branch)
        self.assertEqual(sha, wanted, "the pre-interruption commit must still be HEAD")
        self.assertTrue(os.path.exists(os.path.join(self.dir, "work.txt")),
                        "work committed before the interruption must be restored")

    def test_the_guard_still_refuses_when_no_such_branch_exists(self):
        """Re-attaching must not weaken RLY-166: a detached worktree with NO matching branch is
        still the dangerous case, because commits there would reach nothing."""
        self.git("checkout", "-q", "--detach", "main")
        j = job("exclusive_shell", run="true", id="nj-2", run_id="r1",
                vars={"ref": "RLY-1", "branch": "never-created"})
        outcome, detail, _sha, _session = relay.run_node_job(j, self.dir, self.control)

        self.assertEqual(outcome, "failed")
        self.assertIn("detached", detail)

    def test_shared_clean_is_left_detached_on_purpose(self):
        """exec-clean is created --detach deliberately and never commits. Re-attaching it (or
        guarding it) is what broke every Spec and Plan node when the guard first shipped."""
        self.git("checkout", "-q", "--detach", "main")
        j = job(node_type="shell", run="true", id="nj-3", run_id="r1",
                vars={"ref": "RLY-1", "branch": "rly-1-feature"})
        outcome, _detail, _sha, _session = relay.run_node_job(j, self.dir, self.control)

        self.assertEqual(outcome, "succeeded")
        self.assertEqual(self.head_ref(), "", "shared_clean must stay detached")

    def test_a_job_already_on_its_branch_is_untouched(self):
        branch = "rly-1-feature"
        self.git("checkout", "-q", "-b", branch)
        j = job("exclusive_shell", run="true", id="nj-4", run_id="r1",
                vars={"ref": "RLY-1", "branch": branch})
        outcome, _detail, _sha, _session = relay.run_node_job(j, self.dir, self.control)

        self.assertEqual(outcome, "succeeded")
        self.assertEqual(self.head_ref(), "refs/heads/" + branch)


class OutcomeCommandTest(unittest.TestCase):
    """RLY-175: agents must not hand-write JSON.

    RLY-163 made every agent node declare an outcome, which was right — silent success was
    shipping empty specs. But it also meant every agent writes JSON through a shell heredoc,
    and a long `detail` containing a quote or newline produces invalid JSON. That is exactly
    how the drill-3 run died: final_review's outcome file failed to parse, the executor
    correctly reported `failed`, and the PARSE ERROR was handed to final_fix as its findings.

    So the writing is done by code. These tests use real files and deliberately awkward text.
    """

    def setUp(self):
        import tempfile
        self.dir = tempfile.mkdtemp(prefix="relay-outcome-test-")
        self.path = os.path.join(self.dir, "outcome.json")
        self._env = os.environ.get("RELAY_NODE_OUTCOME")
        os.environ["RELAY_NODE_OUTCOME"] = self.path
        self.addCleanup(self._restore)

    def _restore(self):
        import shutil
        if self._env is None:
            os.environ.pop("RELAY_NODE_OUTCOME", None)
        else:
            os.environ["RELAY_NODE_OUTCOME"] = self._env
        shutil.rmtree(self.dir, ignore_errors=True)

    def run_cli(self, *argv):
        args = relay.build_parser().parse_args(list(argv))
        return args.func(args)

    def read(self):
        with open(self.path) as f:
            return json.load(f)

    def test_writes_valid_json_with_outcome_and_detail(self):
        self.run_cli("outcome", "succeeded", "--detail", "did the thing")
        self.assertEqual(self.read(), {"outcome": "succeeded", "detail": "did the thing"})

    def test_awkward_detail_round_trips_byte_for_byte(self):
        # The shape that actually broke: quotes, an apostrophe, newlines, a backtick, a brace.
        nasty = 'He said "no findings" — it\'s fine.\nLine two `backtick` {brace}\nTrailing'
        self.run_cli("outcome", "failed", "--detail", nasty)
        self.assertEqual(self.read()["detail"], nasty)

    def test_detail_can_come_from_a_file(self):
        src = os.path.join(self.dir, "findings.md")
        body = '## Findings\n\n1. `foo/bar.ex:12` — "quoted" thing\n2. another\n'
        with open(src, "w") as f:
            f.write(body)
        self.run_cli("outcome", "failed", "--detail", "@" + src)
        self.assertEqual(self.read()["detail"], body)

    def test_detail_is_optional(self):
        self.run_cli("outcome", "succeeded")
        self.assertEqual(self.read(), {"outcome": "succeeded", "detail": ""})

    def test_rejects_an_outcome_outside_the_closed_set(self):
        with self.assertRaises(SystemExit):
            capture(self.run_cli, "outcome", "definitely-not-an-outcome", "--detail", "x")

    def test_errors_clearly_when_the_env_var_is_missing(self):
        os.environ.pop("RELAY_NODE_OUTCOME", None)
        with self.assertRaises(SystemExit):
            capture(self.run_cli, "outcome", "succeeded", "--detail", "x")

    def test_the_written_file_is_what_determine_agent_outcome_reads(self):
        """End-to-end on the contract: what the CLI writes is what the executor reports."""
        self.run_cli("outcome", "partial", "--detail", 'half "done"')
        saved = relay.get_card
        relay.get_card = lambda ref: {"status": "working"}
        self.addCleanup(setattr, relay, "get_card", saved)
        outcome, detail = relay.determine_agent_outcome({"vars": {"ref": "RLY-1"}}, True, self.path)
        self.assertEqual((outcome, detail), ("partial", 'half "done"'))


