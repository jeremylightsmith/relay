        relay.worktree_path = lambda name: "/tmp/" + name
        self.resets = []
        relay.reset_worktree = lambda path, base: self.resets.append(path)
        self.reports = []
        relay.report_outcome = lambda *a: (self.reports.append(a) or "done")
