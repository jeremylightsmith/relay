        j = job("exclusive_shell", run="x", id="nj-4", run_id="r4", vars={"ref": "RLY-4"})
        slot, reset = self.pool.assign(j)
        relay.execute_one(j, slot, reset, relay.JobControl(), self.pool)
        self.assertEqual(self.reports[0][:2], ("nj-4", "failed"))
