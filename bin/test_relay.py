def job(case="shared_clean_agent", vars=None, **overrides):
    """A node-job dict in the shape the server GENUINELY sends (RLY-176).

    Built from test/fixtures/executor_contract.json, which is generated by
    test/relay_web/controllers/api/executor_contract_test.exs from the real /api/node-jobs
    routes. Before this existed every test here hand-built its own idea of a job, so this
    suite could be green while the integration could not work — RLY-163/165/166/170/175 all
    shared that one cause.

    Overrides are STRICT on purpose: a test cannot invent a field the server does not send,
    and a test that overrides a field the server has renamed fails loudly instead of quietly
    testing a shape nobody produces.

    `case` is one of the fixture's claim cases:
      shared_clean_agent — the Spec/Plan shape; vars carry `branch` (build_payload sets it
                           unconditionally) even though the shared worktree is detached
      exclusive_shell    — the Code shape, isolation "exclusive"
      resumed_agent      — a needs-input re-entry, resume_session populated
    """
    payload = copy.deepcopy(CONTRACT["claim"][case])

    unknown = sorted(set(overrides) - set(payload))
    if unknown:
        raise KeyError(
            f"job(case={case!r}): {unknown} is not sent by the server. "
            f"The claim payload's fields are {sorted(payload)}."
        )

    if vars:
        unknown_vars = sorted(set(vars) - set(payload["vars"]))
        if unknown_vars:
            raise KeyError(
                f"job(case={case!r}): vars {unknown_vars} are not sent by the server. "
                f"The claim payload's vars are {sorted(payload['vars'])}."
            )
        payload["vars"].update(vars)

    payload.update(overrides)
    return payload

def capture(fn, *args, **kwargs):
    out = io.StringIO()
    with contextlib.redirect_stdout(out):
        fn(*args, **kwargs)
    return out.getvalue()


def capture_ret(fn, *args, **kwargs):
    """Like capture(), but discards stdout and returns the function's return value."""
    with contextlib.redirect_stdout(io.StringIO()):
        return fn(*args, **kwargs)


class _FakeResp:
    """Minimal stand-in for a urlopen() result: a context manager with .read()."""

    def __init__(self, body=b"{}"):
        self._body = body

    def __enter__(self):
        return self

    def __exit__(self, *a):
        return False

    def read(self):
        return self._body


def _http_error(code, body=b""):
    return urllib.error.HTTPError("http://x", code, "err", {}, io.BytesIO(body))


class ClaimContractKeysTest(unittest.TestCase):
    """The exact key set the executor reads off a claim, spelled out here on purpose.

    This is the backstop: job() only catches a rename for fields some test overrides, but the
    executor reads `agent`, `node_id`, `sub_task` and friends without any test naming them.
    Spelling the sets out means renaming ANY server field fails this suite immediately.
    """

    TOP_LEVEL = {"id", "run_id", "ref", "node_id", "node_type", "agent", "run",
                 "isolation", "resume_session", "vars"}
    VARS = {"ref", "branch", "prior_detail", "findings", "sub_task"}

    def test_every_claim_case_has_exactly_the_keys_the_executor_reads(self):
        for case, payload in CONTRACT["claim"].items():
            self.assertEqual(set(payload), self.TOP_LEVEL, case)
            self.assertEqual(set(payload["vars"]), self.VARS, case)

    def test_nullable_vars_are_present_rather_than_omitted(self):
        """bin/relay documents that prior_detail/findings arrive as nil. Dropping them from
        the fixture would let that behaviour drift silently, so pin their presence."""
        for case, payload in CONTRACT["claim"].items():
            for key in ("prior_detail", "findings", "sub_task"):
                self.assertIn(key, payload["vars"], f"{case}.{key}")

    def test_a_shared_clean_job_carries_a_branch_var(self):
        """The RLY-166 case. build_payload attaches `branch` to EVERY job, so a guard keyed
        on the presence of that var refuses every Spec/Plan node — which is exactly what
        shipped, tested against a hand-built job that had no branch."""
        case = CONTRACT["claim"]["shared_clean_agent"]
        self.assertEqual(case["isolation"], "shared_clean")
        self.assertIsNotNone(case["vars"]["branch"])

    def test_the_exclusive_case_is_the_code_shape(self):
        self.assertEqual(CONTRACT["claim"]["exclusive_shell"]["isolation"], "exclusive")

    def test_the_resumed_case_pins_resume_session(self):
        self.assertIsNotNone(CONTRACT["claim"]["resumed_agent"]["resume_session"])
        self.assertIsNone(CONTRACT["claim"]["shared_clean_agent"]["resume_session"])

    def test_the_builder_refuses_a_field_the_server_does_not_send(self):
        with self.assertRaises(KeyError) as caught:
            job(nonexistent_key="x")
        self.assertIn("nonexistent_key", str(caught.exception))

    def test_the_builder_refuses_a_var_the_server_does_not_send(self):
        with self.assertRaises(KeyError) as caught:
            job(vars={"nonexistent_var": "x"})
        self.assertIn("nonexistent_var", str(caught.exception))

    def test_the_builder_applies_overrides_and_keeps_the_rest_of_the_shape(self):
        built = job("exclusive_shell", vars={"branch": "feature-x"}, id="nj-1", run="true")
        self.assertEqual(built["id"], "nj-1")
        self.assertEqual(built["run"], "true")
        self.assertEqual(built["vars"]["branch"], "feature-x")
        self.assertEqual(set(built), self.TOP_LEVEL)

    def test_the_builder_does_not_mutate_the_loaded_fixture(self):
        job(id="nj-mutated", vars={"ref": "MUT-1"})
        self.assertNotEqual(CONTRACT["claim"]["shared_clean_agent"]["id"], "nj-mutated")
        self.assertNotEqual(CONTRACT["claim"]["shared_clean_agent"]["vars"]["ref"], "MUT-1")


class ReverseContractTest(unittest.TestCase):
    """What the executor POSTs, checked against what the controller was proven to accept.

    The ExUnit generator posts the fixture's outcome.request / heartbeat.request bodies at the
    REAL routes, so if these key sets match the fixture, both directions are pinned by one
    file: a controller that stops reading `git_sha`, or an executor that stops sending it, is
    caught on the next run of either suite.
    """

    def test_outcome_body_key_set_matches_the_fixture(self):
        body = relay.outcome_body("succeeded", "done", "abc123", "sess-1")
        self.assertEqual(set(body), set(CONTRACT["outcome"]["request"]))

    def test_outcome_body_defaults_session_id_to_none_rather_than_omitting_it(self):
        self.assertIsNone(relay.outcome_body("succeeded", "done", "abc123")["session_id"])

    def test_heartbeat_body_key_set_matches_the_fixture(self):
        # Omitted (steady-state) beats are a SUBSET of the fixture's request keys;
        # `capabilities` is optional and send-on-change (RLY-182).
        body = relay.heartbeat_body({"name": "box"}, {"shared_clean": 1}, ["nj-1"])
        self.assertTrue(set(body) <= set(CONTRACT["heartbeat"]["request"]))
        # A beat that DOES carry the inventory matches the fixture's key set exactly.
        full = relay.heartbeat_body({"name": "box"}, {"shared_clean": 1}, ["nj-1"],
                                    {"agents": [], "skills": []})
        self.assertEqual(set(full), set(CONTRACT["heartbeat"]["request"]))
        self.assertIn("want_capabilities", CONTRACT["heartbeat"]["response"])

    def test_the_outcome_response_carries_the_run_state_the_executor_reads(self):
        # report_outcome() returns resp["run_state"]; ExecutorPool.release() branches on it.
        self.assertIn("run_state", CONTRACT["outcome"]["response"])

    def test_the_heartbeat_response_carries_the_revoked_ids_the_executor_reads(self):
        # ExecutorHeartbeat._beat() hands each of these to on_revoke().
        self.assertIn("revoked", CONTRACT["heartbeat"]["response"])

    def test_the_claim_request_carries_the_executor_version(self):
        # RLY-184: the server refuses a claim whose executor has no version, so this key is
        # the difference between working and 409 — pinned on both sides of the fixture.
        self.assertIn("version", CONTRACT["claim_request"]["executor"])

    def test_the_heartbeat_request_carries_the_executor_version(self):
        self.assertIn("version", CONTRACT["heartbeat"]["request"]["executor"])

    def test_the_executor_actually_sends_that_version(self):
        self.assertIn("version", relay.executor_ident({"name": "box"}))

    def test_the_heartbeat_response_carries_the_outdated_verdict(self):
        self.assertIn("executor_outdated", CONTRACT["heartbeat"]["response"])
        self.assertIn("required_version", CONTRACT["heartbeat"]["response"])


